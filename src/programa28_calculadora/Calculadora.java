    private void btnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgualActionPerformed
        // TODO add your handling code here:
        VarUno = (Double.parseDouble(String.valueOf(display.getText())));
        if (contadorsuma > 0){
            totalDouble = VarUno + VarUno;
            display.setText(String.valueOf(totalDouble));
            VarUno = 0;
            VarUno = 0;
            contadorsuma = 0;
        }
        if (contadorresta > 0){
            totalDouble = VarUno - VarUno;
            display.setText(String.valueOf(totalDouble));
            VarUno = 0;
            VarUno = 0;
            contadorresta = 0;
        }
        if (contadormultipliacion > 0){
            totalDouble = VarUno * VarUno;
            display.setText(String.valueOf(totalDouble));
            VarUno = 0;
            VarUno = 0;
            contadormultipliacion = 0;
        }
        if (contadordivicion > 0){
            totalDouble = VarUno / VarUno;
            display.setText(String.valueOf(totalDouble));
            VarUno = 0;
            VarUno = 0;
            contadordivicion = 0;
        }
        if (contadorpotencia > 0){
            double n;
            n = VarUno;
            totalDouble = VarUno ;
            while (n > 1){
                totalDouble = totalDouble * VarUno;
                n = n - 1;
            }
            display.setText(String.valueOf(totalDouble));
            VarUno = 0;
            VarUno = 0;
            contadorpotencia = 0;
        }
    }//GEN-LAST:event_btnIgualActionPerformed

